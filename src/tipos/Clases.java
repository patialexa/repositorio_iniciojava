package tipos;

public class Clases {

    public static void main(String[] args) {
        System.out.println("HOLA MUNDO");
        //System.exit(0);
        
        System.out.println("LLEGO HASTA AQUI");
        
        System.out.println(Math.sqrt(2));
        //Sacar el primer caracter de una cadena
                   // 01
        String x = "Hola Mundo";
        System.out.println(x.charAt(0));
                     //0123456
        String x1 = "Hola Mundo";
        System.out.println("r = " + x1.substring(2, 4));//inicio, inicio+2
        System.out.println("r = " + x1.substring(5, 8));//inicio, inicio+3
 
    }
    //Clases, String, System, Math
}

